import 'package:flutter_modular/flutter_modular_test.dart';
import 'package:flutter_test/flutter_test.dart';

import 'package:domestic_budget_app/app/modules/initial/pages/login/login_controller.dart';
import 'package:domestic_budget_app/app/modules/initial/initial_module.dart';

void main() {
  initModule(InitialModule());
  LoginController login;

  setUp(() {
    login = InitialModule.to.get<LoginController>();
  });

  group('LoginController Test', () {
    test("First Test", () {
      expect(login, isInstanceOf<LoginController>());
    });

    test("Set Value", () {
      expect(login.value, equals(0));
      login.increment();
      expect(login.value, equals(1));
    });
  });
}
